  CREATE OR REPLACE FUNCTION "VERIFICA_SALARIO" (PI_COD_EMP NUMBER) RETURN BOOLEAN IS
  DEP_ID EMPLOYEES.DEPARTMENT_ID%TYPE;
  EMPNO EMPLOYEES.EMPLOYEE_ID%TYPE;
  SAL EMPLOYEES.SALARY%TYPE;
  SAL_MEDIO EMPLOYEES.SALARY%TYPE;
  
BEGIN
  EMPNO := PI_COD_EMP;
  SELECT SALARY, DEPARTMENT_ID
    INTO SAL, DEP_ID
    FROM EMPLOYEES
  WHERE EMPLOYEE_ID = EMPNO;
  
  SELECT AVG(SALARY)
  INTO SAL_MEDIO
  FROM EMPLOYEES
  WHERE DEPARTMENT_ID = DEP_ID;
  
  IF SAL > SAL_MEDIO THEN
   RETURN TRUE;
  ELSE
  RETURN FALSE;
  END IF;
  END VERIFICA_SALARIO;

